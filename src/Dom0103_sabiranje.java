public class Dom0103_sabiranje {
    public static void main(String[] args) {

        int broj1 = 17;
        int broj2 = 8;
        System.out.println("Zbir brojeva 17 i 8 je " + (broj1 + broj2));
        System.out.println("Proizvod brojeva 17 i 8 je " + broj1 * broj2);
        System.out.println("Ostatak pri deljenju broja 17 sa brojem 8 je " + (broj1 % broj2));

    }
}
